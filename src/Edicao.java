    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        int id = Integer.parseInt(jTextField8.getText());
        String Nome = jTextField1.getText();
        String BI = jTextField2.getText();
        int Dia = Integer.parseInt(jTextField3.getText());
        String Mes = jComboBox3.getSelectedItem().toString();
        int ano = Integer.parseInt(jTextField5.getText());
        String sexo = jComboBox1.getSelectedItem().toString();
        String Categoria = jComboBox2.getSelectedItem().toString();
        int telefone = Integer.parseInt(jTextField6.getText());
        String morada = jTextField7.getText();
        
        
        Cadastro cadastro = new Cadastro();
        cadastro.setId(id);
        cadastro.setNome(Nome);
        cadastro.setBI(BI);
        cadastro.setDia(Dia);
        cadastro.setMes(Mes);
        cadastro.setAno(ano);
        cadastro.setSexo(sexo);
        cadastro.setCategoria(Categoria);
        cadastro.setTelefone(telefone);
        cadastro.setMorada(morada);

        CadastroDao caddao = new CadastroDao();
        caddao.editar(cadastro);
        jTextField8.setText("");
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField7.setText("");

        JOptionPane.showMessageDialog(rootPane, "Dados Guardados");

        
        

    }//GEN-LAST:event_jButton1ActionPerformed


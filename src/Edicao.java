    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        int idPesquisa = Integer.parseInt(jTextField4.getText());
        
        CadastroDao dao = new CadastroDao();
        Cadastro cadastro = dao.getCadastro(idPesquisa);
        
        if (cadastro == null){
            
            JOptionPane.showMessageDialog(this, "NÃ£o encontrado");
            
        } else {
     
        jTextField8.setText(String.valueOf(cadastro.getId()));       
        jTextField1.setText(cadastro.getNome());
        jTextField2.setText(cadastro.getBI());   
        jTextField3.setText(String.valueOf(cadastro.getDia()));
        jComboBox3.setSelectedItem(cadastro.getMes());       
        jTextField5.setText(String.valueOf(cadastro.getAno()));  
        jComboBox1.setSelectedItem(cadastro.getSexo());
        jComboBox2.setSelectedItem(cadastro.getCategoria());                
        jTextField6.setText(String.valueOf(cadastro.getTelefone()));             
        jTextField7.setText(cadastro.getMorada());
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

